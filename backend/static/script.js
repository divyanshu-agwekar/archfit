async function predictImage() {
  const fileInput = document.getElementById("imageInput");
  const resultDiv = document.getElementById("result");
  const button = document.getElementById("predictBtn");

  if (!fileInput.files.length) {
    alert("Please select an X-ray image first.");
    return;
  }

  const file = fileInput.files[0];
  const allowedTypes = ["image/jpeg", "image/jpg", "image/png"];

  if (!allowedTypes.includes(file.type)) {
    alert("Only JPG, JPEG, and PNG files are allowed.");
    fileInput.value = "";
    return;
  }

  const formData = new FormData();
  formData.append("image", file);

  resultDiv.style.display = "block";
  resultDiv.className = "result-card";
  resultDiv.innerHTML = `
    <div class="loading-box">
      <div class="spinner"></div>
      <h2>Analyzing X-ray...</h2>
      <p>Please wait while ArchFit processes the uploaded image.</p>
    </div>
  `;

  button.disabled = true;
  button.innerText = "Analyzing...";

  try {
    const response = await fetch("/predict", {
      method: "POST",
      body: formData
    });

    const data = await response.json();

    if (data.error) {
      resultDiv.innerHTML = `<h2>Error</h2><p>${data.error}</p>`;
      return;
    }

    const isFlat = data.prediction.toLowerCase().includes("flat");
    resultDiv.className = isFlat ? "result-card flat" : "result-card normal";

    resultDiv.innerHTML = `
      <div class="result-layout">
        <div>
          <img class="result-img" src="${URL.createObjectURL(file)}">
        </div>

        <div>
          <h2 class="result-heading">${data.prediction}</h2>

          <div class="confidence-row">
            <span>Model Confidence</span>
            <span>${data.confidence}%</span>
          </div>

          <div class="confidence-bar">
            <div class="confidence-fill" style="width:${data.confidence}%"></div>
          </div>

          <div class="result-boxes">
            <div class="result-box">
              <h4>Foot Type</h4>
              <p>${data.prediction}</p>
            </div>

            <div class="result-box">
              <h4>Recommended</h4>
              <p>${data.recommended_shoes.join(", ")}</p>
            </div>

            <div class="result-box">
              <h4>Avoid</h4>
              <p>${data.avoid.join(", ")}</p>
            </div>
          </div>

          <div class="medical-note">
            This result is generated by a machine learning model and should be verified by a medical expert for real health decisions.
          </div>
        </div>
      </div>
    `;

  } catch (error) {
    resultDiv.style.display = "block";
    resultDiv.innerHTML = `
      <h2>Something went wrong</h2>
      <p>Please try again after a few seconds.</p>
    `;
  }

  button.disabled = false;
  button.innerText = "Analyze My X-Ray";
}

document.getElementById("imageInput").addEventListener("change", function(e) {
  const preview = document.getElementById("preview");
  const uploadText = document.getElementById("uploadText");
  const file = e.target.files[0];

  if (!file) return;

  const allowedTypes = ["image/jpeg", "image/jpg", "image/png"];

  if (!allowedTypes.includes(file.type)) {
    alert("Only JPG, JPEG, and PNG files are allowed.");
    e.target.value = "";
    preview.style.display = "none";
    uploadText.style.display = "block";
    return;
  }

  preview.src = URL.createObjectURL(file);
  preview.style.display = "block";
  uploadText.style.display = "none";
});